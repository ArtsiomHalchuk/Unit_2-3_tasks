public class T05 {

	public static void main(String[] args) {
		System.out.println("Рассчет среднего арифметического 2 чисел");
		//объявление переменных
		double a;
		double b;
		double c;
		
		//Ввод переменных
		a=1;
		b=2;
		
		//Вывод значений переменных на экран
		System.out.println("A="+a);
		System.out.println("B="+b);
		
		//Нахождение среднего арифметического
		c=(a+b)/2;
		
		//вывод значения среднего арифметического на экран
		System.out.println("Среднее арифметическое равно "+c);

	}

}
